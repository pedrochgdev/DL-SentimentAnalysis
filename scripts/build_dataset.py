import torch
from datasets import load_dataset, concatenate_datasets, DatasetDict, Features, Value

NUM_CORES = 2
MAX_PER_SOURCE = 10000

FEATURES = Features({
    "text": Value("string"),
    "label": Value("int32")
})

# Detectar dispositivo
device = torch.device("cuda" if torch.cuda.is_available() else "cpu")

# Ajustar MAX_LENGTH según el dispositivo
if device.type == "cuda":
    MAX_LENGTH = 512
else:
    MAX_LENGTH = 256

print(f"Usando: {device}")
print(f"MAX_LENGTH = {MAX_LENGTH}")

"""# 2. Funciones Utilitarias"""

import re
import os
import hashlib
from transformers import AutoTokenizer

def clean_text(text):
    text = re.sub(r'<[^>]+>', ' ', text)
    text = re.sub(r'http\S+|www\S+', '', text)
    text = re.sub(r'[^a-zA-Z0-9\s.,!?\'"-]', '', text)
    text = re.sub(r'\s+', ' ', text).strip()
    return text.lower()

def is_valid(text):
    return len(text.split()) >= 5

def sample_balanced(dataset, n=MAX_PER_SOURCE):
    pos = dataset.filter(lambda x: x["label"] == 1, num_proc=NUM_CORES)
    neg = dataset.filter(lambda x: x["label"] == 0, num_proc=NUM_CORES)

    pos = pos.shuffle(seed=42).select(range(min(n // 2, len(pos))))
    neg = neg.shuffle(seed=42).select(range(min(n // 2, len(neg))))

    return concatenate_datasets([pos, neg])

def stable_hash(text: str) -> str:
    return hashlib.md5(text.encode("utf-8")).hexdigest()

def add_hash_column(dataset):
    return dataset.map(
        lambda x: {"_hash": stable_hash(x["text"])},
        num_proc=NUM_CORES
    )

def deduplicate_dataset(dataset):
    dataset = add_hash_column(dataset)

    seen = set()
    keep_indices = []

    for i, h in enumerate(dataset["_hash"]):
        if h not in seen:
            seen.add(h)
            keep_indices.append(i)

    dataset = dataset.select(keep_indices)
    dataset = dataset.remove_columns(["_hash"])

    return dataset

def tokenize_function(example, tokenizer):
    return tokenizer(
        example["text"],
        padding="max_length",
        truncation=True,
        max_length=MAX_LENGTH,
    )

"""# 3. Carga de Datos"""

print("Cargando IMDb...")
imdb = load_dataset("stanfordnlp/imdb", split="train[:15000]")

imdb = imdb.map(
    lambda x: {"text": clean_text(x["text"]), "label": int(x["label"])},
    num_proc=NUM_CORES,
    remove_columns=imdb.column_names,
    features=FEATURES
)

print("Cargando SST-2...")
sst2 = load_dataset("stanfordnlp/sst2", split="train[:15000]")

sst2 = sst2.filter(lambda x: x["label"] != -1, num_proc=NUM_CORES)

sst2 = sst2.map(
    lambda x: {"text": clean_text(x["sentence"]), "label": int(x["label"])},
    num_proc=NUM_CORES,
    remove_columns=sst2.column_names,
    features=FEATURES
)

print("Cargando Yelp...")
yelp = load_dataset("Yelp/yelp_review_full", split="train[:15000]")

yelp = yelp.filter(lambda x: x["label"] != 2, num_proc=NUM_CORES)

yelp = yelp.map(
    lambda x: {
        "text": clean_text(x["text"]),
        "label": int(0 if x["label"] <= 1 else 1)
    },
    num_proc=NUM_CORES,
    remove_columns=yelp.column_names,
    features=FEATURES
)

print("Cargando Amazon...")
amazon = load_dataset("fancyzhx/amazon_polarity", split="train[:15000]")

amazon = amazon.map(
    lambda x: {
        "text": clean_text(x["content"]),
        "label": int(x["label"])
    },
    num_proc=NUM_CORES,
    remove_columns=amazon.column_names,
    features=FEATURES
)

"""# 4. Preprocesamiento

## 4.1. Balanceo y Filtrado
"""

print("Balanceando datasets...")

imdb   = sample_balanced(imdb)
sst2   = sample_balanced(sst2)
yelp   = sample_balanced(yelp)
amazon = sample_balanced(amazon)

print("Uniendo datasets...")
full = concatenate_datasets([imdb, sst2, yelp, amazon])

print("Filtrando texto inválido...")
full = full.filter(lambda x: is_valid(x["text"]), num_proc=NUM_CORES)

print("Deduplicando...")
full = deduplicate_dataset(full)

print("Shuffle y split...")
full = full.shuffle(seed=42)

n = len(full)
train_end = int(n * 0.8)
val_end   = int(n * 0.9)

dataset_final = DatasetDict({
    "train":      full.select(range(0, train_end)),
    "validation": full.select(range(train_end, val_end)),
    "test":       full.select(range(val_end, n))
})

print(f"\nTrain: {len(dataset_final['train'])} | "
        f"Val: {len(dataset_final['validation'])} | "
        f"Test: {len(dataset_final['test'])}")

"""## 4.2. Tokenización"""

print("Tokenizando datasets...")

tokenizer = AutoTokenizer.from_pretrained("distilbert-base-uncased")

tokenized_datasets = dataset_final.map(
    tokenize_function,
    batched=True,
    num_proc=NUM_CORES,
    fn_kwargs={"tokenizer": tokenizer}
)

tokenized_datasets.set_format(
    type="torch",
    columns=["input_ids", "attention_mask", "label"]
)